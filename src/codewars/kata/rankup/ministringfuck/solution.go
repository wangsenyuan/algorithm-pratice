package main

import (
	"bytes"
	"fmt"
)

func Interpreter(code string) string {
	cell := 0

	var buf bytes.Buffer

	for i := 0; i < len(code); i++ {
		if code[i] == '+' {
			cell = (cell + 1) % 256
		} else if code[i] == '.' {
			buf.WriteByte(byte(cell))
		}
	}

	return buf.String()
}

