func solve(n int, B []int) []int {
	A := make([]int, n)

	A[0] = B[0]

	x := max(0, A[0])

	for i := 1; i < n; i++ {
		A[i] = B[i] + x
		x = max(x, A[i])
	}

	return A
}

func max(a, b int) int {
	if a >= b {
		return a
	}
	return b
}
