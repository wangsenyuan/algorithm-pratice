func main() {
	scanner := bufio.NewScanner(os.Stdin)

	n, q := readTwoNums(scanner)
	A := readNNums(scanner, n)

	solver := NewSolver(n, A)

	for q > 0 {
		q--
		l, r := readTwoNums(scanner)
		res := solver.solve(l, r)
		if res {
			fmt.Println("YES")
		} else {
			fmt.Println("NO")
		}
	}
}

type Solver struct {
	n int
	A []int
}

func NewSolver(n int, A []int) Solver {
	return Solver{n, A}
}

func (solver *Solver) solve(l int, r int) bool {
	l--
	r--

	if l+1 >= r {
		return false
	}

	A := solver.A

	if A[l] < A[l+1] {
		return A[r-1] > A[r]
	}

	if A[l] > A[l+1] {
		return A[r-1] < A[r]
	}
	return false
}
