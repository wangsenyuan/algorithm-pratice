const MOD = 1000000007

func solve(l, r int64) int64 {

	var act, req, val, ans int64

	for i := 0; i < 60; i++ {
		val = min(r-l+1, req-act+1)
		val %= MOD

		if l&(1<<uint(i)) > 0 {
			ans += (val * (1 << uint(i)) % MOD) % MOD
			ans %= MOD
			act += 1 << uint(i)
		}
		req += 1 << uint(i)
	}

	return ans
}

func min(a, b int64) int64 {
	if a <= b {
		return a
	}
	return b
}
