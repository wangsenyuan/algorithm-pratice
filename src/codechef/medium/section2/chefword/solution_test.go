func runSample(t *testing.T, n int, K int, word string, mat [][]float64, words []string, expect float64) {
	ws := make([][]byte, n)

	for i := 0; i < n; i++ {
		ws[i] = []byte(words[i])
	}

	res := solve(n, K, []byte(word), mat, ws)

	if math.Abs(res-expect) > 1e-7 {
		t.Errorf("Sample %d %d %s %v %v, expect %f, but got %f", n, K, word, mat, words, expect, res)
	}
}

