func readFloat64(bs []byte, pos int, r *float64) int {
	var first float64
	var second float64
	exp := 1.0
	var dot bool
	for pos < len(bs) && bs[pos] != ' ' {
		if bs[pos] == '.' {
			dot = true
		} else if !dot {
			first = first*10 + float64(bs[pos]-'0')
		} else {
			second = second*10 + float64(bs[pos]-'0')
			exp *= 10
		}
		pos++
	}
	*r = first + second/exp
	//fmt.Fprintf(os.Stderr, "%.6f ", *r)
	return pos
}

func readNFloats(scanner *bufio.Scanner, n int) []float64 {
	res := make([]float64, n)
	pos := -1
	scanner.Scan()
	for i := 0; i < n; i++ {
		pos = readFloat64(scanner.Bytes(), pos+1, &res[i])
	}
	return res
}

func main() {
	scanner := bufio.NewScanner(os.Stdin)

	tc := readNum(scanner)

	for tc > 0 {
		tc--
		n := readNum(scanner)
		b := readNNums(scanner, n)
		p := readNFloats(scanner, n)
		res := solve(n, b, p)
		fmt.Printf("%.7f\n", res)
	}
}

func solve(n int, b []int, p []float64) float64 {
	fmt.Fprintf(os.Stderr, "%d %v %v\n", n, b, p)

	var res float64

	for j := 0; j < 31; j++ {
		var prob float64
		for i := 0; i < n; i++ {
			if b[i]&(1<<uint(j)) > 0 {
				prob = prob * (1.0 - p[i]) + (1.0 - prob) * p[i]
			}
		}
		x := 1 << uint(j)
		res += prob * float64(x)
	}

	return res
}
