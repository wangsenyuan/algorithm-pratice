func main() {
	scanner := bufio.NewScanner(os.Stdin)

	tc := readNum(scanner)

	for tc > 0 {
		tc--
		n := readNum(scanner)
		A := readNNums(scanner, n)
		fmt.Println(solve(n, A))
	}
}

func solve(n int, A []int) int64 {
	if n == 0 {
		return 0
	}
	if n == 1 {
		return int64(A[0])
	}

	if n == 2 {
		return max(int64(A[0])+2*int64(A[1]), 2*int64(A[0])+int64(A[1]))
	}

	a := int64(A[0])
	b := max(int64(A[0])+2*int64(A[1]), 2*int64(A[0])+int64(A[1]))

	for i := 2; i < n; i++ {
		x := b + int64(i+1)*int64(A[i])
		y := a + int64(i)*int64(A[i]) + int64(i+1)*int64(A[i-1])
		a = b
		b = max(x, y)
	}

	return b
}

func max(a, b int64) int64 {
	if a >= b {
		return a
	}
	return b
}
