func main() {
	scanner := bufio.NewScanner(os.Stdin)

	tc := readNum(scanner)

	for tc > 0 {
		tc--
		n := readNum(scanner)

		arr := readNNums(scanner, n)

		fmt.Println(solve(n, arr))
	}

}

func solve(n int, arr []int) int {
	var i = 1

	for i < n && arr[i] > arr[i-1] {
		i++
	}

	if i == n {
		return n*(n+1)/2 - 1
	}

	// delete arrary from j, where 0 < j <= i
	res := i

	j := n - 1

	for j >= i {
		if j < n-1 && arr[j] >= arr[j+1] {
			break
		}
		k := sort.Search(i, func(k int) bool {
			return arr[k] >= arr[j]
		})
		// arr[0...k) + arr[j...]
		res += k + 1

		j--
	}

	return res
}
