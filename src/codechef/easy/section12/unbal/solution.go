func readNInt64Nums(scanner *bufio.Scanner, n int) []int64 {
	res := make([]int64, n)
	x := -1
	scanner.Scan()
	for i := 0; i < n; i++ {
		x = readInt64(scanner.Bytes(), x+1, &res[i])
	}
	return res
}

func main() {
	scanner := bufio.NewScanner(os.Stdin)

	tc := readNum(scanner)

	for tc > 0 {
		tc--

		n, k := readTwoNums(scanner)

		fmt.Println(solve(n, k))
	}
}

func solve(n, k int) string {
	if k == 2 || k == 4 || k == n {
		return "-1"
	}

	var buf bytes.Buffer

	p, r := n/(k-2), n%(k-2)

	for i := 0; i < p; i++ {
		for j := 0; j < k-2; j++ {
			if j < (k-2)/2 {
				buf.WriteByte('(')
			} else {
				buf.WriteByte(')')
			}
		}
	}

	if r != 2 {
		for j := 0; j < r; j++ {
			if j < r/2 {
				buf.WriteByte('(')
			} else {
				buf.WriteByte(')')
			}
		}
		return buf.String()
	}
	return "(" + buf.String() + ")"
}
