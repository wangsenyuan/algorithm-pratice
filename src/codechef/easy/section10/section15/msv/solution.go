func main() {
	scanner := bufio.NewScanner(os.Stdin)

	tc := readNum(scanner)

	for tc > 0 {
		tc--
		n := readNum(scanner)
		arr := readNNums(scanner, n)
		r := solve(n, arr)
		fmt.Println(r)
	}
}

func solve(n int, arr []int) int {
	pos := make(map[int]int)

	for i := 0; i < n; i++ {
		pos[arr[i]] = i
	}

	dp := make([]int, n)

	for i := n - 2; i >= 0; i-- {
		num := arr[i]

		for x := 1; x*x <= num; x++ {
			if num%x == 0 {

				if _, found := pos[x]; found && pos[x] > i {
					dp[pos[x]]++
				}
				y := num / x
				if x != y {
					if _, found := pos[y]; found && pos[y] > i {
						dp[pos[y]]++
					}
				}
			}
		}
	}
	var best int

	for i := 0; i < n; i++ {
		best = max(best, dp[i])
	}

	return best
}

func max(a, b int) int {
	if a >= b {
		return a
	}
	return b
}
