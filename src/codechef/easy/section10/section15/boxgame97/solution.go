func main() {
	scanner := bufio.NewScanner(os.Stdin)

	tc := readNum(scanner)

	for tc > 0 {
		tc--
		line := readNNums(scanner, 3)
		n, k, p := line[0], line[1], line[2]
		arr := readNNums(scanner, n)
		res := solve(n, k, p, arr)
		fmt.Println(res)
	}
}

func solve(n, k, p int, arr []int) int {

	process := func(f1 func(int, int) int, f2 func(int, int) int) int {
		if k%2 == 1 {
			res := arr[0]

			for i := 1; i < n; i++ {
				res = f1(res, arr[i])
			}
			return res
		}

		res := arr[1]

		for i := 1; i < n-1; i++ {
			x := f2(arr[i-1], arr[i+1])
			res = f1(res, x)
		}

		res = f1(res, arr[n-2])

		return res
	}

	if p == 0 {
		return process(max, min)
	}
	return process(min, max)
}

func min(a, b int) int {
	if a <= b {
		return a
	}
	return b
}

func max(a, b int) int {
	if a >= b {
		return a
	}
	return b
}
