package main

import "testing"

func runSample(t *testing.T, A []int64, k int, expect int64) {
	res := solve(A, k)

	if res != expect {
		t.Fatalf("Sample expect %d, but got %d", expect, res)
	}
}

func TestSample1(t *testing.T) {
	A := []int64{9, 14, 28, 1, 7, 13, 15, 29, 2, 31}
	k := 4
	var expect int64 = 24
	runSample(t, A, k, expect)
}

