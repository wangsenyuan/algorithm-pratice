func readNInt64Nums(scanner *bufio.Scanner, n int) []int64 {
	res := make([]int64, n)
	x := -1
	scanner.Scan()
	for i := 0; i < n; i++ {
		x = readInt64(scanner.Bytes(), x+1, &res[i])
	}
	return res
}

func main() {
	scanner := bufio.NewScanner(os.Stdin)

	tc := readNum(scanner)

	for tc > 0 {
		tc--
		_, p := readTwoNums(scanner)
		scanner.Scan()
		bs := scanner.Bytes()
		solve(bs, p)
		fmt.Println(string(bs))
	}
}

func solve(S []byte, P int) {
	use := make([]int, len(S))

	i, j := 0, len(S)-1

	for i < j && P > 0 {
		if S[j] == 'b' {
			j--
			continue
		}
		for i < j && S[i] == 'a' {
			i++
		}

		if i >= j {
			break
		}
		// S[i] == b, & S[j] == a, swap them
		S[i], S[j] = S[j], S[i]
		P--
		use[j]++
		i++
		j--
	}
	if P == 0 {
		return
	}
	// the string must be aaaabbb
	for i < len(S) && P > 0 {
		if 2-use[i] <= P {
			S[i] = 'a'
			P -= (2 - use[i])
		}
		i++
	}
}
