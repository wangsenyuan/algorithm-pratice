func main() {
	scanner := bufio.NewScanner(os.Stdin)

	tc := readNum(scanner)

	for tc > 0 {
		tc--
		n := readNum(scanner)
		grid := make([]string, n)
		for i := 0; i < n; i++ {
			scanner.Scan()
			grid[i] = scanner.Text()
		}

		fmt.Println(solve(n, grid))
	}
}

func solve(n int, grid []string) int {
	var total int
	var first int
	for i := 0; i < n; i++ {
		for j := 0; j < n; j++ {
			if grid[i][j] == '1' {
				total++
				if j+1 <= n/2 {
					first++
				}
			}

		}
	}

	ans := abs(total - 2*first)

	for i := 0; i < n; i++ {
		var a, b int
		for j := 0; j < n; j++ {
			if grid[i][j] == '1' {
				if j+1 <= n/2 {
					a++
				}
			}
			if grid[i][n-1-j] == '1' {
				if j+1 <= n/2 {
					b++
				}
			}
		}
		x := first - a + b
		y := total - x
		d := abs(x - y)
		ans = min(ans, d)
	}

	return ans
}

func abs(x int) int {
	if x < 0 {
		return -x
	}
	return x
}

func min(a, b int) int {
	if a <= b {
		return a
	}
	return b
}
