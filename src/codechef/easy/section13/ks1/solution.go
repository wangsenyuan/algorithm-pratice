func main() {
	scanner := bufio.NewScanner(os.Stdin)

	tc := readNum(scanner)

	for tc > 0 {
		tc--
		n := readNum(scanner)
		A := readNNums(scanner, n)
		res := solve(n, A)
		fmt.Println(res)
	}
}

const M = 1000001

var count = make([]int, M)
var bad = make([]int, M)

func solve(n int, A []int) int64 {
	for i := 0; i < M; i++ {
		count[i] = 0
		bad[i] = 0
	}
	count[0] = 1
	bad[0] = 1
	var res int64
	var prev int
	for i := 1; i <= n; i++ {
		prev ^= A[i-1]
		res += int64(count[prev])*int64(i) - int64(bad[prev])
		count[prev]++
		bad[prev] += i + 1
	}

	return res
}
