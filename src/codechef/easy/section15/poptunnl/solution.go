func main() {
	scanner := bufio.NewScanner(os.Stdin)
	tc := readNum(scanner)

	for tc > 0 {
		tc--
		n, K := readTwoNums(scanner)
		S := make([]string, n)
		for i := 0; i < n; i++ {
			scanner.Scan()
			S[i] = scanner.Text()
		}
		fmt.Println(solve(n, K, S))
	}
}

func solve(n, K int, S []string) int {
	if S[0][0] == '0' {
		return -1
	}

	dist := make([]int, n)

	for i := 0; i < n; i++ {
		dist[i] = -1
	}

	dist[0] = 0
	que := make([]int, n)
	var front, end int
	que[end] = 0
	end++

	for front < end {
		u := que[front]
		front++
		x := max(0, u-K)
		y := min(n-1, u+K)
		for v := x; v <= y; v++ {
			if S[u][v] == '1' && dist[v] < 0 {
				dist[v] = dist[u] + 1
				que[end] = v
				end++
			}
		}
	}

	return dist[n-1]
}

func max(a, b int) int {
	if a >= b {
		return a
	}
	return b
}

func min(a, b int) int {
	if a <= b {
		return a
	}
	return b
}
