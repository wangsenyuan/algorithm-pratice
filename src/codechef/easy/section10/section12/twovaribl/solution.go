func main() {
	scanner := bufio.NewScanner(os.Stdin)

	tc := readNum(scanner)

	for tc > 0 {
		tc--
		x := readNum(scanner)
		fmt.Println(solve(x))
	}
}

const INF = 1e9

func solve(xf int) int {
	X := int64(xf)
	var r int
	var x, y int64
	for {
		p := find(y)
		x = p
		if x > X {
			return r
		}
		y += p * p
		r++
	}
}

func find(y int64) int64 {
	var left int64 = 1
	var right int64 = INF

	for left < right {
		mid := (left + right) / 2
		if mid*mid > y {
			right = mid
		} else {
			left = mid + 1
		}
	}
	return right
}
