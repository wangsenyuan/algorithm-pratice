import "testing"

func runSample(t *testing.T, nums []int, x int, k int, expect int) {
	res := minOperations(nums, x, k)
	if res != int64(expect) {
		t.Errorf("Sample expect %d, but got %d", expect, res)
	}
}

func TestSample1(t *testing.T) {
	nums := []int{5, -2, 1, 3, 7, 3, 6, 4, -1}
	x := 3
	k := 2
	expect := 8
	runSample(t, nums, x, k, expect)
}

