func main() {
	scanner := bufio.NewScanner(os.Stdin)

	tc := readNum(scanner)

	for tc > 0 {
		tc--
		arr := readNNums(scanner, 3)
		res := solve(arr)
		if res {
			fmt.Println("YES")
		} else {
			fmt.Println("NO")
		}
	}
}

func solve(arr []int) bool {
	sum := arr[0] + 2*arr[1] + 3*arr[2]
	if sum%2 == 1 {
		return false
	}

	sum /= 2

	for cc, j := min(sum/3, arr[2]), 0; cc >= 0 && j < 2; cc, j = cc-1, j+1 {
		ssum := sum - cc*3
		for bb, jj := min(ssum/2, arr[1]), 0; bb >= 0 && jj < 3; bb, jj = bb-1, jj+1 {
			if ssum-2*bb <= arr[0] {
				return true
			}
		}
	}

	return false
}

func min(a, b int) int {
	if a <= b {
		return a
	}
	return b
}
