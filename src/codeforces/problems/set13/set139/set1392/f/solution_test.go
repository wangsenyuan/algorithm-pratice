package main

import (
	"reflect"
	"testing"
)

func runSample(t *testing.T, H []int64, expect []int64) {
	res := solve(len(H), H)

	if !reflect.DeepEqual(res, expect) {
		t.Errorf("Sample expect %v, but got %v", expect, res)
	}
}

func TestSample1(t *testing.T) {
	H := []int64{2, 6, 7, 8}
	expect := []int64{5, 5, 6, 7}
	runSample(t, H, expect)
}

