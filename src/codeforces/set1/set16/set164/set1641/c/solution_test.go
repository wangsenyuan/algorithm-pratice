package main

import (
	"reflect"
	"testing"
)

func runSample(t *testing.T, n int, Q [][]int, expect []string) {
	res := solve(n, Q)

	if !reflect.DeepEqual(res, expect) {
		t.Fatalf("Sample expect %v, but got %v", expect, res)
	}
}

func TestSample1(t *testing.T) {
	n := 6
	Q := [][]int{
		{0, 4, 5, 0},
		{1, 5},
		{1, 6},
		{0, 4, 6, 1},
		{1, 6},
		{0, 2, 5, 1},
		{0, 2, 2, 0},
		{1, 3},
		{1, 2},
	}
	expect := []string{
		"NO",
		"N/A",
		"YES",
		"YES",
		"NO",
	}
	runSample(t, n, Q, expect)
}

