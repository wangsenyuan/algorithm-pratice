func main() {
	scanner := bufio.NewScanner(os.Stdin)

	tc := readNum(scanner)

	for tc > 0 {
		tc--
		n := readNum(scanner)

		grid := make([]string, n)

		for i := 0; i < n; i++ {
			scanner.Scan()
			grid[i] = scanner.Text()
		}
		fmt.Println(solve(n, grid))
	}
}

const INF = 1 << 30

func solve(N int, grid []string) int64 {

	row := make([]int, N)

	for i := 0; i < N; i++ {
		for j := 0; j < N; j++ {
			if grid[i][j] == '1' {
				row[i] = j
				break
			}
		}
	}
	var res int64

	for i := 0; i < N; i++ {
		a := INF
		b := -INF
		for j := i; j < N; j++ {
			a = min(a, row[j])
			b = max(b, row[j])

			if b-a == j-i {
				res++
			}
		}
	}
	return res
}

func max(a, b int) int {
	if a >= b {
		return a
	}
	return b
}

func min(a, b int) int {
	if a <= b {
		return a
	}
	return b
}
