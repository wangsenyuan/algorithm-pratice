func readNNums(reader *bufio.Reader, n int) []int {
	res := make([]int, n)
	x := 0
	bs, _ := reader.ReadBytes('\n')
	for i := 0; i < n; i++ {
		for x < len(bs) && (bs[x] < '0' || bs[x] > '9') && bs[x] != '-' {
			x++
		}
		x = readInt(bs, x, &res[i])
	}
	return res
}

func process(reader *bufio.Reader) int {
	n := readNum(reader)
	points := make([][]int, n)
	for i := 0; i < n; i++ {
		points[i] = readNNums(reader, 2)
	}
	return solve(points)
}

const inf = 1 << 30

func solve(points [][]int) int {
	n := len(points)

	sort.Slice(points, func(i, j int) bool {
		return points[i][0] < points[j][0] || points[i][0] == points[j][0] && points[i][1] < points[j][1]
	})

	check := func(dist int) bool {
		// 最小值和最大值
		y := []int{inf, -inf}
		for l, r := 0, 0; r < n; r++ {
			for l <= r && points[r][0]-points[l][0] >= dist {
				y[0] = min(y[0], points[l][1])
				y[1] = max(y[1], points[l][1])
				l++
			}
			if y[1]-points[r][1] >= dist || points[r][1]-y[0] >= dist {
				return true
			}
		}
		return false
	}

	l, r := 0, inf

	for l < r {
		mid := (l + r) / 2
		if check(mid) {
			l = mid + 1
		} else {
			r = mid
		}
	}

	return r - 1
}
func min(a, b int) int {
	if a <= b {
		return a
	}
	return b
}

func max(a, b int) int {
	if a >= b {
		return a
	}
	return b
}
